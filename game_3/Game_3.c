static void g3_seg_display_digit(uint8_t digit, uint8_t dp_on) {
    /* bit order: A B C D E F G */
    static const uint8_t lut[10] = {
        0x7E, /* 0: A B C D E F */
        0x30, /* 1: B C */
        0x6D, /* 2: A B D E G */
        0x79, /* 3: A B C D G */
        0x33, /* 4: B C F G */
        0x5B, /* 5: A C D F G */
        0x5F, /* 6: A C D E F G */
        0x70, /* 7: A B C */
        0x7F, /* 8 */
        0x7B  /* 9: A B C D F G */
    };
    uint8_t m = lut[digit % 10U];
    dp_on = dp_on ? 1U : 0U;
    if (g3_seg_cache_valid && g3_seg_last_mask == m && g3_seg_last_dp == dp_on) {
        return;
    }
    g3_seg_cache_valid = 1U;
    g3_seg_last_mask = m;
    g3_seg_last_dp = dp_on;
    g3_seg_write(G3_SEG_A_PORT, G3_SEG_A_PIN, (m & 0x40U) != 0U);
    g3_seg_write(G3_SEG_B_PORT, G3_SEG_B_PIN, (m & 0x20U) != 0U);
    g3_seg_write(G3_SEG_C_PORT, G3_SEG_C_PIN, (m & 0x10U) != 0U);
    g3_seg_write(G3_SEG_D_PORT, G3_SEG_D_PIN, (m & 0x08U) != 0U);
    g3_seg_write(G3_SEG_E_PORT, G3_SEG_E_PIN, (m & 0x04U) != 0U);
    g3_seg_write(G3_SEG_F_PORT, G3_SEG_F_PIN, (m & 0x02U) != 0U);
    g3_seg_write(G3_SEG_G_PORT, G3_SEG_G_PIN, (m & 0x01U) != 0U);
    g3_seg_write(G3_SEG_DP_PORT, G3_SEG_DP_PIN, dp_on);
}

static void g3_seg_display_segment(uint8_t segment) {
    g3_seg_all_off();
    g3_seg_cache_valid = 0U;
    switch (segment & 7U) {
        case 0: g3_seg_write(G3_SEG_A_PORT, G3_SEG_A_PIN, 1); break;
        case 1: g3_seg_write(G3_SEG_B_PORT, G3_SEG_B_PIN, 1); break;
        case 2: g3_seg_write(G3_SEG_C_PORT, G3_SEG_C_PIN, 1); break;
        case 3: g3_seg_write(G3_SEG_D_PORT, G3_SEG_D_PIN, 1); break;
        case 4: g3_seg_write(G3_SEG_E_PORT, G3_SEG_E_PIN, 1); break;
        case 5: g3_seg_write(G3_SEG_F_PORT, G3_SEG_F_PIN, 1); break;
        case 6: g3_seg_write(G3_SEG_G_PORT, G3_SEG_G_PIN, 1); break;
        default: g3_seg_write(G3_SEG_DP_PORT, G3_SEG_DP_PIN, 1); break;
    }
}

static void g3_sevenseg_update(void) {
    uint8_t value = 0U;
    uint8_t dp = 0U;

#if G3_SEG_WIRING_TEST
    uint32_t elapsed = HAL_GetTick() - g3_mode_enter_ms;
    if (elapsed < (8U * G3_SEG_WIRING_TEST_MS)) {
        g3_seg_display_segment((uint8_t)(elapsed / G3_SEG_WIRING_TEST_MS));
        return;
    }
#endif

    if (g3_mode == G3_MODE_PLAYING || g3_mode == G3_MODE_PAUSED || g3_mode == G3_MODE_LEVEL_CLEAR) {
        g3_seg_show_hp = (uint8_t)(((HAL_GetTick() / 1000U) & 1U) != 0U);
        if (g3_seg_show_hp) {
            value = g3_player.hp;
            dp = 1U;                  /* decimal point means HP */
        } else {
            value = (uint8_t)(g3_level + 1U);
            dp = 0U;                  /* no decimal point means Level */
        }
        g3_seg_display_digit(value, dp);
    } else if (g3_mode == G3_MODE_DIFFICULTY) {
        g3_seg_display_digit((uint8_t)(g3_difficulty + 1U), 0U);
    } else if (g3_mode == G3_MODE_KNIGHT_SELECT || g3_mode == G3_MODE_RULES) {
        g3_seg_display_digit((uint8_t)(g3_selected_knight + 1U), 0U);
    } else {
        g3_seg_display_digit((uint8_t)(g3_stats.score % 10U), 1U);
    }
}

static void g3_update_pot(void) {
    uint32_t now = HAL_GetTick();
    if ((now - g3_last_pot_read_ms) < G3_POT_READ_INTERVAL_MS) return;
    g3_last_pot_read_ms = now;

    ADC_ChannelConfTypeDef sConfig = {0};
    sConfig.Channel = G3_POT_ADC_CHANNEL;
    sConfig.Rank = ADC_REGULAR_RANK_1;
    sConfig.SamplingTime = ADC_SAMPLETIME_47CYCLES_5;
    sConfig.SingleDiff = ADC_SINGLE_ENDED;
    sConfig.OffsetNumber = ADC_OFFSET_NONE;
    sConfig.Offset = 0;

    if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) == HAL_OK) {
        if (HAL_ADC_Start(&hadc1) == HAL_OK) {
            if (HAL_ADC_PollForConversion(&hadc1, 2U) == HAL_OK) {
                g3_pot_adc = (uint16_t)HAL_ADC_GetValue(&hadc1);
                g3_speed_level = (uint8_t)(g3_pot_adc / 1024U);
                if (g3_speed_level > 3U) g3_speed_level = 3U;
            }
            HAL_ADC_Stop(&hadc1);
        }
    }
}

