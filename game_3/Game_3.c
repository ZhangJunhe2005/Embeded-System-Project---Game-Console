static void g3_damage_player(uint8_t respawn_to_start) {
    if (g3_mode != G3_MODE_PLAYING || g3_player.inv_timer > 0U) return;
    if (g3_player.hp > 0U) g3_player.hp--;
    g3_stats.hits_taken++;
    g3_player.inv_timer = g3_cfg()->damage_cooldown_frames;
    g3_rgb_set(1, 0, 0, 420U);
    g3_pwm_flash(100, 420U);
    g3_play_sound(620, 120, 36);
    if (g3_player.hp == 0U) { g3_enter_game_over(); return; }
    if (respawn_to_start) {
        g3_stats.deaths++;
        g3_reset_player_for_level();
    } else {
        g3_player.x = (int16_t)(g3_player.x + (g3_player.facing_right ? -14 : 14));
        g3_player.vy = -5;
    }
}

static void g3_move_horizontal(int16_t dx) {
    int8_t step = (dx >= 0) ? 1 : -1;
    uint8_t count = g3_abs_u8(dx);
    for (uint8_t i = 0; i < count; i++) {
        int16_t next_x = (int16_t)(g3_player.x + step);
        if (!g3_rect_has_solid(next_x, g3_player.y, G3_PLAYER_W, G3_PLAYER_H)) g3_player.x = next_x;
        else { g3_player.vx = 0; break; }
    }
}

static void g3_move_vertical(int16_t dy) {
    int8_t step = (dy >= 0) ? 1 : -1;
    uint8_t count = g3_abs_u8(dy);
    g3_player.on_ground = 0;
    for (uint8_t i = 0; i < count; i++) {
        int16_t next_y = (int16_t)(g3_player.y + step);
        if (!g3_rect_has_solid(g3_player.x, next_y, G3_PLAYER_W, G3_PLAYER_H)) g3_player.y = next_y;
        else {
            if (step > 0) g3_player.on_ground = 1;
            g3_player.vy = 0;
            break;
        }
    }
}

static void g3_handle_input(void) {
    Direction d = joystick_data.direction;
    uint8_t left = (d == W || d == NW || d == SW);
    uint8_t right = (d == E || d == NE || d == SE);
    uint8_t jump = (d == N || d == NE || d == NW);
    uint8_t down = (d == S || d == SE || d == SW);
    const G3_KnightCfg *k = g3_knight();

    g3_player.vx = 0;
    if (left) { g3_player.vx = -(int16_t)k->move_speed; g3_player.facing_right = 0; }
    else if (right) { g3_player.vx = (int16_t)k->move_speed; g3_player.facing_right = 1; }

    if (jump && !g3_last_jump_input && g3_player.on_ground) {
        g3_player.vy = k->jump_vel;
        g3_player.on_ground = 0;
        g3_play_sound(1800, 55, 22);
    }
    g3_last_jump_input = jump;

    if (down && !g3_player.on_ground && g3_player.vy < G3_MAX_FALL_SPEED) {
        g3_player.vy = (int16_t)(g3_player.vy + 2);
    }

    if (current_input.btn2_down && g3_player.attack_timer == 0U && g3_player.attack_cd == 0U) {
        g3_player.attack_timer = G3_ATTACK_FRAMES;
        g3_player.attack_cd = k->attack_cooldown_frames;
        g3_pwm_flash(75, 180U);
        g3_play_sound(k->attack_freq_hz, 65, 28);
    }
}

static void g3_update_camera(void) {
    int16_t max_camera = (int16_t)(G3_LEVEL_COLS * G3_TILE - G3_LCD_W);
    int16_t auto_target;
    int16_t player_target;
    int16_t target;
    uint32_t now = HAL_GetTick();
    uint32_t interval = g3_cfg()->auto_scroll_div;

    if (g3_speed_level == 1U && interval > 8U) interval -= 8U;
    else if (g3_speed_level == 2U && interval > 14U) interval -= 14U;
    else if (g3_speed_level >= 3U && interval > 20U) interval -= 20U;
    if (interval < 10U) interval = 10U;

    if ((now - g3_last_auto_scroll_ms) >= interval && g3_auto_camera_x < max_camera) {
        g3_last_auto_scroll_ms = now;
        g3_auto_camera_x = (int16_t)(g3_auto_camera_x + 1 + (g3_speed_level >= 3U ? 1 : 0));
        if (g3_auto_camera_x > max_camera) g3_auto_camera_x = max_camera;
    }

    auto_target = g3_auto_camera_x;
    player_target = (int16_t)(g3_player.x - 88);
    if (player_target < auto_target) player_target = auto_target;
    if (player_target < 0) player_target = 0;
    if (player_target > max_camera) player_target = max_camera;
    target = player_target;

    if ((target - g3_camera_x) > 12 || (g3_camera_x - target) > 12) g3_camera_x = g3_approach_i16(g3_camera_x, target, 5);
    else g3_camera_x = g3_approach_i16(g3_camera_x, target, 2);
}


static void g3_get_attack_rect(int16_t *ax, int16_t *ay, int16_t *aw, int16_t *ah) {
    const G3_KnightCfg *k = g3_knight();
    *ay = (int16_t)(g3_player.y + 4);
    if (k->attack_style == G3_ATTACK_LANCE) { *aw = 34; *ah = 6; *ay = (int16_t)(g3_player.y + 5); }
    else if (k->attack_style == G3_ATTACK_AXE) { *aw = 22; *ah = 18; *ay = (int16_t)(g3_player.y - 1); }
    else if (k->attack_style == G3_ATTACK_ARC) { *aw = 48; *ah = 8; *ay = (int16_t)(g3_player.y + 3); }
    else { *aw = 22; *ah = 10; }
    *ax = g3_player.facing_right ? (int16_t)(g3_player.x + G3_PLAYER_W) : (int16_t)(g3_player.x - *aw);
}

static void g3_process_attack_hits(void) {
    if (g3_player.attack_timer == 0U) return;
    int16_t ax, ay, aw, ah;
    g3_get_attack_rect(&ax, &ay, &aw, &ah);
    for (uint8_t i = 0; i < G3_MAX_ENEMIES; i++) {
        G3_Enemy *e = &g3_enemies[i];
        if (e->alive && g3_aabb(ax, ay, aw, ah, e->x, e->y, G3_ENEMY_W, G3_ENEMY_H)) {
            e->alive = 0;
            g3_stats.kills++;
            g3_stats.score += 40U;
            g3_rgb_set(0, 1, 0, 360U);
            g3_pwm_flash(85, 300U);
            g3_play_sound(2600, 85, 30);
        }
    }
}

static void g3_update_enemies(void) {
    const G3_DiffCfg *cfg = g3_cfg();
    for (uint8_t i = 0; i < G3_MAX_ENEMIES; i++) {
        G3_Enemy *e = &g3_enemies[i];
        if (!e->alive) continue;
        int16_t next_x = (int16_t)(e->x + e->dir * (int8_t)cfg->enemy_speed);
        if (next_x <= e->min_x || next_x >= e->max_x) {
            e->dir = (int8_t)-e->dir;
            next_x = (int16_t)(e->x + e->dir * (int8_t)cfg->enemy_speed);
        }
        int16_t front_foot_x = (e->dir > 0) ? (int16_t)(next_x + G3_ENEMY_W) : next_x;
        int16_t foot_y = (int16_t)(e->y + G3_ENEMY_H + 1);
        if (!g3_tile_is_solid(g3_get_tile_colrow(g3_floor_div_tile(front_foot_x), g3_floor_div_tile(foot_y)))) e->dir = (int8_t)-e->dir;
        else if (!g3_rect_has_solid(next_x, e->y, G3_ENEMY_W, G3_ENEMY_H)) e->x = next_x;
        else e->dir = (int8_t)-e->dir;
        if (g3_aabb(g3_player.x, g3_player.y, G3_PLAYER_W, G3_PLAYER_H, e->x, e->y, G3_ENEMY_W, G3_ENEMY_H)) {
            g3_damage_player(0);
        }
    }
}

static void g3_collect_tiles(void) {
    int16_t left = g3_floor_div_tile(g3_player.x);
    int16_t right = g3_floor_div_tile((int16_t)(g3_player.x + G3_PLAYER_W - 1));
    int16_t top = g3_floor_div_tile(g3_player.y);
    int16_t bottom = g3_floor_div_tile((int16_t)(g3_player.y + G3_PLAYER_H - 1));
    for (int16_t row = top; row <= bottom; row++) {
        for (int16_t col = left; col <= right; col++) {
            char tile = g3_get_tile_colrow(col, row);
            if (g3_tile_is_collectable(tile)) {
                if (g3_player.hp < g3_player.max_hp) g3_player.hp++;
                g3_stats.score += 25U;
                g3_set_tile(col, row, G3_TILE_EMPTY);
                g3_play_sound(1300, 70, 24);
            } else if (tile == G3_TILE_FLAG) {
                if (g3_level >= (G3_LEVEL_COUNT - 1U)) g3_enter_game_win();
                else g3_enter_level_clear();
                return;
            }
        }
    }
}

static void g3_update_playing(void) {
    g3_handle_input();

    g3_bg_scroll_px = (uint16_t)(g3_bg_scroll_px + 1U + g3_speed_level + (uint16_t)((g3_player.vx != 0) ? (g3_abs_u8(g3_player.vx) / 2U) : 0U));

    if (g3_player.vx != 0) g3_move_horizontal(g3_player.vx);

    g3_player.vy = (int16_t)(g3_player.vy + G3_GRAVITY);
    if (g3_player.vy > G3_MAX_FALL_SPEED) g3_player.vy = G3_MAX_FALL_SPEED;
    g3_move_vertical(g3_player.vy);

    if (g3_player.attack_timer > 0U) g3_player.attack_timer--;
    if (g3_player.attack_cd > 0U) g3_player.attack_cd--;
    if (g3_player.inv_timer > 0U) g3_player.inv_timer--;

    if (g3_player.x < 0) g3_player.x = 0;
    if (g3_player.x > (int16_t)(G3_LEVEL_COLS * G3_TILE - G3_PLAYER_W)) {
        g3_player.x = (int16_t)(G3_LEVEL_COLS * G3_TILE - G3_PLAYER_W);
    }

    g3_process_attack_hits();
    g3_update_enemies();
    g3_collect_tiles();

    if (g3_mode != G3_MODE_PLAYING) return;

    if (g3_rect_has_hazard(g3_player.x, g3_player.y, G3_PLAYER_W, G3_PLAYER_H)) g3_damage_player(0);
    if (g3_player.y > (int16_t)(G3_MAP_ROWS * G3_TILE + 18)) g3_damage_player(1);

    if (g3_player.inv_timer == 0U && g3_player.attack_timer == 0U && g3_pwm_flash_until_ms == 0U) {
        uint8_t duty = (uint8_t)(18U + (g3_player.hp * 56U) / g3_player.max_hp);
        g3_pwm_set_duty(duty);
    }

    g3_update_camera();
}


