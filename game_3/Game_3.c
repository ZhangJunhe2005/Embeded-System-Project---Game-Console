static void g3_safe_rect(int16_t x, int16_t y, int16_t w, int16_t h, uint8_t colour, uint8_t fill) {
    if (w <= 0 || h <= 0) return;
    int16_t x0 = x;
    int16_t y0 = y;
    int16_t x1 = (int16_t)(x + w - 1);
    int16_t y1 = (int16_t)(y + h - 1);
    if (x1 < 0 || y1 < 0 || x0 >= G3_LCD_W || y0 >= G3_LCD_H) return;
    if (x0 < 0) x0 = 0;
    if (y0 < 0) y0 = 0;
    if (x1 >= G3_LCD_W) x1 = G3_LCD_W - 1;
    if (y1 >= G3_LCD_H) y1 = G3_LCD_H - 1;
    if (fill) {
        for (int16_t yy = y0; yy <= y1; yy++) {
            LCD_Draw_HLine_Fast(x0, x1, yy, colour);
        }
        return;
    }
    LCD_Draw_Rect((uint16_t)x0, (uint16_t)y0,
                  (uint16_t)(x1 - x0 + 1), (uint16_t)(y1 - y0 + 1), colour, fill);
}

static void g3_safe_line(int16_t x0, int16_t y0, int16_t x1, int16_t y1, uint8_t colour) {
    if ((x0 < 0 && x1 < 0) || (x0 >= G3_LCD_W && x1 >= G3_LCD_W) ||
        (y0 < 0 && y1 < 0) || (y0 >= G3_LCD_H && y1 >= G3_LCD_H)) return;
    if (x0 < 0) x0 = 0;
    if (x1 < 0) x1 = 0;
    if (y0 < 0) y0 = 0;
    if (y1 < 0) y1 = 0;
    if (x0 >= G3_LCD_W) x0 = G3_LCD_W - 1;
    if (x1 >= G3_LCD_W) x1 = G3_LCD_W - 1;
    if (y0 >= G3_LCD_H) y0 = G3_LCD_H - 1;
    if (y1 >= G3_LCD_H) y1 = G3_LCD_H - 1;
    LCD_Draw_Line((uint16_t)x0, (uint16_t)y0, (uint16_t)x1, (uint16_t)y1, colour);
}

static void g3_safe_circle(int16_t x, int16_t y, int16_t r, uint8_t colour, uint8_t fill) {
    if (r <= 0) return;
    if (x - r < 0 || y - r < 0 || x + r >= G3_LCD_W || y + r >= G3_LCD_H) {
        g3_safe_rect((int16_t)(x - r), (int16_t)(y - r), (int16_t)(2 * r + 1), (int16_t)(2 * r + 1), colour, fill);
        return;
    }
    LCD_Draw_Circle((uint16_t)x, (uint16_t)y, (uint16_t)r, colour, fill);
}

static int16_t g3_approach_i16(int16_t current, int16_t target, int16_t step) {
    if (current < target) {
        current = (int16_t)(current + step);
        if (current > target) current = target;
    } else if (current > target) {
        current = (int16_t)(current - step);
        if (current < target) current = target;
    }
    return current;
}

