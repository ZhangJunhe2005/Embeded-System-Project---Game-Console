static void g3_draw_platform(int16_t sx, int16_t sy) {
    uint8_t base = (g3_difficulty == G3_DIFF_EASY) ? 12 : ((g3_difficulty == G3_DIFF_NORMAL) ? 13 : 1);
    uint8_t top = (g3_difficulty == G3_DIFF_HARD) ? 6 : G3_COL_PLATFORM_HI;
    g3_safe_rect(sx, sy, G3_TILE, G3_TILE, base, 1);
    g3_safe_rect(sx, sy, G3_TILE, 3, top, 1);
    g3_safe_rect((int16_t)(sx + 1), (int16_t)(sy + 5), (int16_t)(G3_TILE - 2), 2, G3_COL_PANEL, 1);
    g3_safe_rect((int16_t)(sx + 3), (int16_t)(sy + 8), 3, 2, G3_COL_PANEL, 1);
    g3_safe_rect((int16_t)(sx + 8), (int16_t)(sy + 8), 2, 2, G3_COL_PANEL, 1);
}

static void g3_draw_spike(int16_t sx, int16_t sy) {
    /* clearer hazard barrier */
    g3_safe_rect(sx, (int16_t)(sy + G3_TILE - 2), G3_TILE, 2, G3_COL_PANEL, 1);
    g3_safe_rect((int16_t)(sx + 1), (int16_t)(sy + 4), (int16_t)(G3_TILE - 2), 6, G3_COL_SPIKE, 1);
    g3_safe_rect((int16_t)(sx + 2), (int16_t)(sy + 5), (int16_t)(G3_TILE - 4), 4, G3_COL_WARN, 1);
    g3_safe_line((int16_t)(sx + 2), (int16_t)(sy + 4), (int16_t)(sx + 5), sy, G3_COL_SPIKE);
    g3_safe_line((int16_t)(sx + G3_TILE - 3), (int16_t)(sy + 4), (int16_t)(sx + G3_TILE - 6), sy, G3_COL_SPIKE);
    g3_safe_line((int16_t)(sx + 4), (int16_t)(sy + 6), (int16_t)(sx + G3_TILE - 5), (int16_t)(sy + 9), G3_COL_PANEL);
    g3_safe_line((int16_t)(sx + 4), (int16_t)(sy + 9), (int16_t)(sx + G3_TILE - 5), (int16_t)(sy + 6), G3_COL_PANEL);
}

static void g3_draw_heal(int16_t sx, int16_t sy) {
    g3_safe_rect((int16_t)(sx + 3), (int16_t)(sy + 5), 7, 3, G3_COL_HP, 1);
    g3_safe_rect((int16_t)(sx + 5), (int16_t)(sy + 3), 3, 7, G3_COL_HP, 1);
}

static void g3_draw_map(void) {
    int16_t first_col = g3_floor_div_tile(g3_camera_x);
    int16_t last_col = g3_floor_div_tile((int16_t)(g3_camera_x + G3_LCD_W)) + 1;
    for (int16_t row = 0; row < G3_MAP_ROWS; row++) {
        for (int16_t col = first_col; col <= last_col; col++) {
            char tile = g3_get_tile_colrow(col, row);
            if (tile == G3_TILE_EMPTY) continue;
            int16_t sx = (int16_t)(col * G3_TILE - g3_camera_x);
            int16_t sy = (int16_t)(G3_UI_H + row * G3_TILE);
            if (tile == G3_TILE_SOLID) g3_draw_platform(sx, sy);
            else if (tile == G3_TILE_SPIKE) g3_draw_spike(sx, sy);
            else if (tile == G3_TILE_FLAG) g3_draw_sprite_safe(sx, sy, 16, 16, g3_flag_sprite, 0);
            else if (tile == G3_TILE_HEAL) g3_draw_heal(sx, sy);
        }
    }
}

static void g3_draw_enemy(const G3_Enemy *e) {
    int16_t sx = (int16_t)(e->x - g3_camera_x);
    int16_t sy = (int16_t)(G3_UI_H + e->y - 2);
    if (sx <= -16 || sx >= G3_LCD_W || sy <= -16 || sy >= G3_LCD_H) return;
    g3_safe_rect((int16_t)(sx + 3), (int16_t)(sy + 15), 10, 2, G3_COL_PANEL, 1);
    g3_draw_enemy_avatar((int16_t)(sx + 1), (int16_t)(sy + 1), 1, (uint8_t)(e->dir < 0));
}


static void g3_draw_enemies(void) {
    for (uint8_t i = 0; i < G3_MAX_ENEMIES; i++) if (g3_enemies[i].alive) g3_draw_enemy(&g3_enemies[i]);
}

static void g3_draw_attack(int16_t sx, int16_t sy) {
    int16_t ax, ay, aw, ah;
    g3_get_attack_rect(&ax, &ay, &aw, &ah);
    ax = (int16_t)(ax - g3_camera_x);
    ay = (int16_t)(G3_UI_H + ay);
    const G3_KnightCfg *k = g3_knight();
    if (k->attack_style == G3_ATTACK_LANCE) {
        g3_safe_line(ax, (int16_t)(ay + 3), (int16_t)(ax + (g3_player.facing_right ? aw : 0)), (int16_t)(ay + 3), G3_COL_ATTACK);
        g3_safe_rect((int16_t)(ax + (g3_player.facing_right ? aw - 4 : 0)), ay, 4, 7, G3_COL_ATTACK, 1);
    } else if (k->attack_style == G3_ATTACK_AXE) {
        g3_safe_circle((int16_t)(ax + aw / 2), (int16_t)(ay + ah / 2), 9, G3_COL_ATTACK, 0);
        g3_safe_line((int16_t)(sx + 8), (int16_t)(sy + 9), (int16_t)(ax + aw / 2), (int16_t)(ay + ah / 2), G3_COL_ATTACK);
    } else if (k->attack_style == G3_ATTACK_ARC) {
        for (uint8_t i = 0; i < 5; i++) g3_safe_circle((int16_t)(ax + (g3_player.facing_right ? i * 9 : aw - i * 9)), (int16_t)(ay + 3 + (i % 2) * 2), 2, G3_COL_ATTACK, 1);
    } else {
        g3_safe_rect(ax, (int16_t)(ay + 3), aw, 3, G3_COL_ATTACK, 1);
        g3_safe_line(ax, (int16_t)(ay + 2), (int16_t)(ax + (g3_player.facing_right ? aw - 1 : 0)), (int16_t)(ay - 2), G3_COL_ATTACK);
    }
}

static void g3_draw_player(void) {
    if (g3_player.inv_timer > 0U && ((g3_player.inv_timer / 3U) & 1U)) return;
    int16_t sx = (int16_t)(g3_player.x - g3_camera_x - 1);
    int16_t sy = (int16_t)(G3_UI_H + g3_player.y);
    g3_safe_rect((int16_t)(sx + 2), (int16_t)(sy + 15), 10, 2, G3_COL_PANEL, 1);
    g3_draw_knight_avatar(g3_knight(), (int16_t)(sx + 1), (int16_t)(sy + 1), 1, (uint8_t)!g3_player.facing_right, 0);
    if (g3_player.attack_timer > 0U) g3_draw_attack(sx, sy);
}


