static uint8_t g3_ws2812_limit_brightness(uint8_t brightness) {
    if (brightness > G3_WS2812_BRIGHTNESS_LIMIT) {
        brightness = G3_WS2812_BRIGHTNESS_LIMIT;
    }
    return brightness;
}

static void g3_ws2812_show_hp_bar(uint8_t active) {
    uint8_t hp = g3_player.hp;
    uint8_t max_hp = g3_player.max_hp;
    uint8_t pixels[G3_WS2812_COUNT * 3U];
    uint8_t lit_count = 0U;
    uint8_t brightness = G3_WS2812_HP_BRIGHTNESS;

    brightness = g3_ws2812_limit_brightness(brightness);

    if (!active || max_hp == 0U) {
        if (g3_ws2812_hp_cache_valid && g3_ws2812_last_bar_active == 0U) {
            return;
        }
        g3_ws2812_hp_cache_valid = 1U;
        g3_ws2812_last_bar_active = 0U;
        g3_ws2812_last_hp = 0U;
        g3_ws2812_last_max_hp = 0U;
        WS2812_Off();
        return;
    }

    if (hp > max_hp) {
        hp = max_hp;
    }

    if (g3_ws2812_hp_cache_valid &&
        g3_ws2812_last_bar_active == 1U &&
        g3_ws2812_last_hp == hp &&
        g3_ws2812_last_max_hp == max_hp) {
        return;
    }

    for (uint8_t i = 0; i < (G3_WS2812_COUNT * 3U); i++) {
        pixels[i] = 0U;
    }

    if (hp > 0U) {
        lit_count = (uint8_t)(((uint16_t)hp * G3_WS2812_COUNT + max_hp - 1U) / max_hp);
        if (lit_count == 0U) {
            lit_count = 1U;
        }
        if (lit_count > G3_WS2812_COUNT) {
            lit_count = G3_WS2812_COUNT;
        }
    }

    for (uint8_t i = 0; i < lit_count; i++) {
        pixels[(uint16_t)i * 3U] = 255U;
    }

    g3_ws2812_hp_cache_valid = 1U;
    g3_ws2812_last_bar_active = 1U;
    g3_ws2812_last_hp = hp;
    g3_ws2812_last_max_hp = max_hp;
    WS2812_Show_Pixels(pixels, G3_WS2812_COUNT, brightness);
}

static void g3_ws2812_show_flash(uint8_t r, uint8_t g, uint8_t b, uint8_t on) {
    uint8_t pixels[G3_WS2812_COUNT * 3U];
    uint8_t brightness = G3_WS2812_FLASH_BRIGHTNESS;

    brightness = g3_ws2812_limit_brightness(brightness);

    if (g3_ws2812_flash_cache_valid &&
        g3_ws2812_last_flash_on == on &&
        g3_ws2812_last_flash_r == r &&
        g3_ws2812_last_flash_g == g &&
        g3_ws2812_last_flash_b == b) {
        return;
    }

    for (uint8_t i = 0; i < (G3_WS2812_COUNT * 3U); i++) {
        pixels[i] = 0U;
    }

    if (on) {
        for (uint8_t i = 0; i < G3_WS2812_COUNT; i++) {
            pixels[(uint16_t)i * 3U] = r;
            pixels[(uint16_t)i * 3U + 1U] = g;
            pixels[(uint16_t)i * 3U + 2U] = b;
        }
    }

    g3_ws2812_flash_cache_valid = 1U;
    g3_ws2812_last_flash_on = on;
    g3_ws2812_last_flash_r = r;
    g3_ws2812_last_flash_g = g;
    g3_ws2812_last_flash_b = b;
    g3_ws2812_hp_cache_valid = 0U;
    WS2812_Show_Pixels(pixels, G3_WS2812_COUNT, brightness);
}

static void g3_ws2812_init(void) {
    g3_ws2812_hp_cache_valid = 0U;
    g3_ws2812_flash_cache_valid = 0U;
    WS2812_Off();
}

static void g3_rgb_set(uint8_t r, uint8_t g, uint8_t b, uint32_t duration_ms) {
    g3_rgb_r = r ? 255U : 0U;
    g3_rgb_g = g ? 255U : 0U;
    g3_rgb_b = b ? 255U : 0U;
    g3_rgb_until_ms = (duration_ms == 0U) ? 0U : (HAL_GetTick() + duration_ms);
    g3_ws2812_flash_cache_valid = 0U;
}

static void g3_rgb_off(void) {
    g3_rgb_set(0, 0, 0, 0);
}

static void g3_pwm_flash(uint8_t duty, uint32_t duration_ms) {
    g3_pwm_flash_duty = duty;
    g3_pwm_flash_until_ms = HAL_GetTick() + duration_ms;
}

static void g3_pwm_set_duty(uint8_t duty) {
    (void)duty;
    g3_ws2812_show_hp_bar((g3_mode == G3_MODE_PLAYING ||
                           g3_mode == G3_MODE_PAUSED ||
                           g3_mode == G3_MODE_LEVEL_CLEAR ||
                           g3_mode == G3_MODE_GAME_OVER ||
                           g3_mode == G3_MODE_GAME_WIN) ? 1U : 0U);
}

static void g3_play_sound(uint32_t freq_hz, uint32_t duration_ms, uint8_t volume) {
    Game3Audio_PlayTone((uint16_t)freq_hz, (uint16_t)duration_ms, volume);
    g3_sound_until_ms = HAL_GetTick() + duration_ms;
}

static void g3_update_sound(void) {
    if (g3_sound_until_ms != 0U && HAL_GetTick() >= g3_sound_until_ms) {
        Game3Audio_Stop();
        g3_sound_until_ms = 0U;
    }
}

static void g3_update_feedback(void) {
    uint32_t now = HAL_GetTick();

    if (g3_rgb_until_ms != 0U) {
        if (now < g3_rgb_until_ms) {
            uint8_t on = (uint8_t)(((now / G3_WS2812_FLASH_INTERVAL_MS) & 1U) == 0U);
            g3_ws2812_show_flash(g3_rgb_r, g3_rgb_g, g3_rgb_b, on);
            return;
        }
        g3_rgb_r = g3_rgb_g = g3_rgb_b = 0U;
        g3_rgb_until_ms = 0U;
        g3_ws2812_flash_cache_valid = 0U;
    }

    if (g3_pwm_flash_until_ms != 0U) {
        if (now >= g3_pwm_flash_until_ms) {
            g3_pwm_flash_until_ms = 0U;
        }
    }

    g3_ws2812_show_hp_bar((g3_mode == G3_MODE_PLAYING ||
                           g3_mode == G3_MODE_PAUSED ||
                           g3_mode == G3_MODE_LEVEL_CLEAR ||
                           g3_mode == G3_MODE_GAME_OVER ||
                           g3_mode == G3_MODE_GAME_WIN) ? 1U : 0U);
}

