static void g3_draw_sprite_safe(int16_t x0, int16_t y0, uint8_t rows, uint8_t cols,
                                const uint8_t *sprite, uint8_t flip_x) {
    for (uint8_t y = 0; y < rows; y++) {
        int16_t sy = (int16_t)(y0 + y);
        if (sy < 0 || sy >= G3_LCD_H) continue;
        for (uint8_t x = 0; x < cols; x++) {
            uint8_t src_x = flip_x ? (uint8_t)(cols - 1U - x) : x;
            uint8_t pixel = sprite[(uint16_t)y * cols + src_x];
            if (pixel != 255U) {
                g3_safe_pixel((int16_t)(x0 + x), sy, pixel);
            }
        }
    }
}

static void g3_draw_sprite_scaled_safe(int16_t x0, int16_t y0, uint8_t rows, uint8_t cols,
                                       const uint8_t *sprite, uint8_t flip_x, uint8_t scale) {
    for (uint8_t y = 0; y < rows; y++) {
        for (uint8_t x = 0; x < cols; x++) {
            uint8_t src_x = flip_x ? (uint8_t)(cols - 1U - x) : x;
            uint8_t pixel = sprite[(uint16_t)y * cols + src_x];
            if (pixel == 255U) continue;
            g3_safe_rect((int16_t)(x0 + x * scale), (int16_t)(y0 + y * scale), scale, scale, pixel, 1);
        }
    }
}

static void g3_print_wrapped_centered(const char *text, uint16_t y, uint8_t colour,
                                      uint8_t font_size, uint8_t max_chars, uint8_t line_gap) {
    char line1[48];
    char line2[48];
    size_t len = strlen(text);
    if (len <= max_chars) {
        g3_print_centered(text, y, colour, font_size);
        return;
    }
    size_t split = max_chars;
    while (split > 0 && text[split] != ' ') split--;
    if (split == 0) split = max_chars;
    memset(line1, 0, sizeof(line1));
    memset(line2, 0, sizeof(line2));
    memcpy(line1, text, split);
    line1[split] = 0;
    while (text[split] == ' ') split++;
    strncpy(line2, text + split, sizeof(line2) - 1U);
    g3_print_centered(line1, y, colour, font_size);
    g3_print_centered(line2, (uint16_t)(y + line_gap), colour, font_size);
}

