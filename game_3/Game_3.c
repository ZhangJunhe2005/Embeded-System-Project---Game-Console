static void g3_render_knight_select(void) {
    char text_buf[64];
    const int16_t spacing = 82;
    const int16_t card_w = 60;
    const int16_t card_h = 102;
    int16_t target = g3_knight_carousel_target_px;
    int16_t delta = (int16_t)(target - g3_knight_carousel_px);

    if (delta > 0) {
        int16_t step = (int16_t)((delta + 1) / 2);
        if (step < 5) step = 5;
        if (step > delta) step = delta;
        g3_knight_carousel_px = (int16_t)(g3_knight_carousel_px + step);
    } else if (delta < 0) {
        int16_t step = (int16_t)((-delta + 1) / 2);
        if (step < 5) step = 5;
        if (step > -delta) step = (int16_t)-delta;
        g3_knight_carousel_px = (int16_t)(g3_knight_carousel_px - step);
    }

    g3_bg_scroll_px++;
    g3_safe_rect(0, 0, G3_LCD_W, G3_LCD_H, G3_COL_BG, 1);
    g3_draw_background_bitmap();
    g3_draw_window_plain(8, 10, 224, 220, G3_COL_FLAG, 0);
    g3_print_centered_shadow("CHOOSE KNIGHT", 22, G3_COL_FLAG, G3_COL_PANEL, 2);
    g3_print_centered("Joystick L/R", 40, G3_COL_TEXT, 1);

    for (uint8_t i = 0; i < G3_KNIGHT_COUNT; i++) {
        int16_t cx = (int16_t)(120 + i * spacing - g3_knight_carousel_px);
        int16_t x = (int16_t)(cx - card_w / 2);
        uint8_t selected = (i == g3_selected_knight);
        uint8_t border = selected ? G3_COL_FLAG : 15;
        uint8_t fill = selected ? 0 : 8;
        uint8_t name_col = selected ? g3_knight_cfgs[i].accent_colour : 15;
        int16_t y = selected ? 52 : 70;
        int16_t h = selected ? card_h : 76;
        uint8_t scale = selected ? 2 : 1;

        if (x > 250 || (x + card_w) < -10) continue;
        g3_draw_card_frame(x, y, card_w, h, border, fill, selected);
        g3_safe_rect((int16_t)(x + 5), (int16_t)(y + 6), (int16_t)(card_w - 10), 13, selected ? 1 : 9, 1);
        LCD_printString(g3_knight_cfgs[i].name, (uint16_t)(x + 8), (uint16_t)(y + 10), name_col, 1);

        if (selected) {
            g3_draw_knight_avatar(&g3_knight_cfgs[i], (int16_t)(x + 14), (int16_t)(y + 22), scale, 0, 0);
            g3_safe_line((int16_t)(x + 10), (int16_t)(y + 68), (int16_t)(x + card_w - 10), (int16_t)(y + 68), 13);
            g3_draw_weapon_icon(g3_knight_cfgs[i].attack_style, (int16_t)(x + 14), (int16_t)(y + 72), 1, g3_knight_cfgs[i].accent_colour, 0);
        } else {
            g3_draw_knight_avatar(&g3_knight_cfgs[i], (int16_t)(x + 22), (int16_t)(y + 24), scale, 0, 0);
            g3_safe_rect((int16_t)(x + 10), (int16_t)(y + 58), (int16_t)(card_w - 20), 4, 13, 1);
        }
    }

    g3_draw_window_plain(18, 170, 204, 36, g3_knight()->accent_colour, 0);
    snprintf(text_buf, sizeof(text_buf), "%s | %s", g3_knight()->weapon, g3_knight()->trait);
    g3_print_centered(text_buf, 178, G3_COL_TEXT, 1);
    g3_print_centered(g3_knight()->desc, 192, G3_COL_TEXT, 1);
    g3_print_centered("BL NEXT   JS BACK", 214, G3_COL_TEXT, 1);
    LCD_Refresh(&cfg0);
}


